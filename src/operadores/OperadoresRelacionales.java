
package operadores;


public class OperadoresRelacionales {
    
    public static void main(String[] args) {
        
        int numero01=130;
        int numero02=67;
        
        // <  (Signo menor que)
        System.out.println("La Operacion es: " + (numero01 < numero02));
        
        // >  (Signo mayor que)
        System.out.println("La Operacion es: " + (numero01 > numero02));
        
        
        // <=  (Signo menor o igual  que)
        System.out.println("La Operacion es: " + (numero01 <= numero02));
        
        // >=  (Signo mayor o igual que)
        System.out.println("La Operacion es: " + (numero01 >= numero02));
        
        // !=  (Diferente de )
        System.out.println("La Operacion es: " + (numero01 != numero02));
        
        // ==  (Igual a ) 
        System.out.println("La Operacion es: " + (numero01 == numero02));
        
        
    }

}
